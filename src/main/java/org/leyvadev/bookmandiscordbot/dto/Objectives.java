package org.leyvadev.bookmandiscordbot.dto;

public class Objectives{
    public Baron baron;
    public Champion champion;
    public Dragon dragon;
    public Inhibitor inhibitor;
    public RiftHerald riftHerald;
    public Tower tower;

    public Baron getBaron() {
        return baron;
    }

    public void setBaron(Baron baron) {
        this.baron = baron;
    }

    public Champion getChampion() {
        return champion;
    }

    public void setChampion(Champion champion) {
        this.champion = champion;
    }

    public Dragon getDragon() {
        return dragon;
    }

    public void setDragon(Dragon dragon) {
        this.dragon = dragon;
    }

    public Inhibitor getInhibitor() {
        return inhibitor;
    }

    public void setInhibitor(Inhibitor inhibitor) {
        this.inhibitor = inhibitor;
    }

    public RiftHerald getRiftHerald() {
        return riftHerald;
    }

    public void setRiftHerald(RiftHerald riftHerald) {
        this.riftHerald = riftHerald;
    }

    public Tower getTower() {
        return tower;
    }

    public void setTower(Tower tower) {
        this.tower = tower;
    }
}
